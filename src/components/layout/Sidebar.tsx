export default function Sidebar() {
  const pathname = usePathname();
  const [collapsed, setCollapsed] = useState(false);
  const navRef = useRef<HTMLElement>(null);
  const scrollPosRef = useRef(0);
  const { hasPermission } = useAuth();

  // Save scroll position before navigation triggers re-render
  useEffect(() => {
    const nav = navRef.current;
    if (!nav) return;
    // Restore saved scroll position after pathname change
    nav.scrollTop = scrollPosRef.current;
  }, [pathname]);

  const handleNavScroll = () => {
    if (navRef.current) {
      scrollPosRef.current = navRef.current.scrollTop;
    }
  };

  const isActive = (href: string) => {
    if (href === "/dashboard") return pathname === "/dashboard";
    return pathname.startsWith(href);
  };

  return (
    <aside
      className={`flex flex-col h-screen bg-background border-r border-border transition-all duration-300 ${
        collapsed ? "w-[68px]" : "w-[260px]"
      }`}
    >
      {/* Logo */}
      <div className="flex items-center h-16 px-4 border-b border-border shrink-0">
        <div className="flex items-center gap-3 overflow-hidden">
          <div className="w-8 h-8 rounded-lg bg-primary flex items-center justify-center shrink-0">
            <span className="text-white font-bold text-sm">S</span>
          </div>
          {!collapsed && (
            <span className="text-lg font-semibold text-text whitespace-nowrap">
              Sellai Admin
            </span>
          )}
        </div>
      </div>

      {/* Navigation */}
      <nav ref={navRef} onScroll={handleNavScroll} className="flex-1 overflow-y-auto py-4 px-3">
        {navGroups.map((group) => {
          const visibleItems = group.items.filter(
            (item) => !item.permission || hasPermission(item.permission)
          );
          if (visibleItems.length === 0) return null;
          return (
          <div key={group.title} className="mb-6">
            {!collapsed && (
              <p className="px-3 mb-2 text-[11px] font-semibold tracking-wider text-text-muted uppercase">
                {group.title}
              </p>
            )}
            <ul className="space-y-1">
              {visibleItems.map((item) => {
                const active = isActive(item.href);
                return (
                  <li key={item.href}>
                    <Link
                      href={item.href}
                      className={`flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium transition-all duration-150 group ${
                        active
                          ? "bg-primary-light text-primary"
                          : "text-text-muted hover:text-text hover:bg-surface-hover"
                      } ${collapsed ? "justify-center" : ""}`}
                      title={collapsed ? item.label : undefined}
                    >
                      <span
                        className={`shrink-0 ${
                          active
                            ? "text-primary"
                            : "text-text-muted group-hover:text-text"
                        }`}
                      >
                        {item.icon}
                      </span>
                      {!collapsed && (
                        <span className="truncate">{item.label}</span>
                      )}
                    </Link>
                  </li>
                );
              })}
            </ul>
          </div>
          );
        })}
      </nav>

      {/* Collapse toggle */}
      <div className="border-t border-border p-3 shrink-0">
        <button
          onClick={() => setCollapsed(!collapsed)}
          className="flex items-center justify-center w-full py-2 rounded-lg text-text-muted hover:text-text hover:bg-surface-hover transition-colors"
          aria-label={collapsed ? "Expand sidebar" : "Collapse sidebar"}
        >
          <svg
            className={`w-5 h-5 transition-transform duration-300 ${
              collapsed ? "rotate-180" : ""
            }`}
            fill="none"
            viewBox="0 0 24 24"
            stroke="currentColor"
            strokeWidth={1.5}
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              d="M18.75 19.5l-7.5-7.5 7.5-7.5m-6 15L5.25 12l7.5-7.5"
            />
          </svg>
        </button>
      </div>
    </aside>
  );
}
